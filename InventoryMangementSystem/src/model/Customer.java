package model;

public class Customer {
    private String cusName;
    private int cusId;

    public void setCusName(String cusName) {
        this.cusName = cusName;
    }

    public String getCusName() {
        return cusName;
    }

    public void setCusId(int cusId) {
        this.cusId = cusId;
    }

    public int getCusId() {
        return cusId;
    }

}










